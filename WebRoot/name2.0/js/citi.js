function FgetProvince() {
	var l = {};
	for ( var k in $_city_list) {
		if (k == 0)
			continue;
		l[k] = $_city_list[k]['n']
	}
	return l
}
function FgetCity(pid) {
	if (pid <= 0 || !(pid in $_city_list))
		return null;
	var l = {};
	for ( var k in $_city_list[pid]) {
		if (k == 0 || k == 'n')
			continue;
		l[pid * 100 + (k - 0)] = $_city_list[pid][k]
	}
	return l
}
function setCitySel() {
	var px = $('prov');
	var cx = $('city');
	var c = FgetCity(px.value);
	cx.options.length = 0;
	for ( var d in c) {
		cx.options.add(new Option(c[d], d))
	}
}
function setProvCitySel() {
	var ps = FgetProvince();
	var px = $('prov');
	var cx = $('city');
	px.options.length = 0;
	for ( var k in ps) {
		if (k == 0 || k == 99) {
			continue;
		}
		px.options.add(new Option(ps[k], k));
	}
	setCitySel();
}

